/**
 * WrapperClass
 */
public class WrapperClass {

    public static void main(String[] args) {
        
        Integer i = Integer.valueOf(10);    // Using factory method

        Integer a = 10;     // Autoboxing method

        byte bb = 15;
        Byte b = Byte.valueOf(bb);

        Byte ab = 15;
    }
}