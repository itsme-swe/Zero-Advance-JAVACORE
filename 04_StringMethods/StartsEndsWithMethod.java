/**
 * startsWith() and endsWith() Method returns boolean value and used to check the begining and end of the string
 */
public class StartsEndsWithMethod {

    public static void main(String[] args) {
        String s1 = "WWW.harh@gmail.com";

        System.out.println(s1.startsWith("WWW"));        // Output: true
        System.out.println(s1.endsWith("com"));       // Output: true
    }
}