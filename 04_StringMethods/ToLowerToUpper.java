/**
 * String methods toLower() and toUpper() to convert cases.
 */
public class ToLowerToUpper {
    public static void main(String[] args) {
        String carName = "virtus";

        String str = carName.toUpperCase();

        System.out.println(str);

        String company = "VOLKSWAGEN";

        String newStr = company.toLowerCase();

        System.out.println(newStr);
    }

    
}